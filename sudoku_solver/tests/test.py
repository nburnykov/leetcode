import unittest

from sudoku_solver.app import SudokuSolver


class TestApp(unittest.TestCase):
    def test_check_square_1(self):
        board = [["5", "3", ".", ".", "7", ".", ".", ".", "."],
                 ["6", ".", ".", "1", "9", "5", ".", ".", "."],
                 [".", "9", "8", ".", ".", ".", ".", "6", "."],
                 ["8", ".", ".", ".", "6", ".", ".", ".", "3"],
                 ["4", ".", ".", "8", ".", "3", ".", ".", "1"],
                 ["7", ".", ".", ".", "2", ".", ".", ".", "6"],
                 [".", "6", ".", ".", ".", ".", "2", "8", "."],
                 [".", ".", ".", "4", "1", "9", ".", ".", "5"],
                 [".", ".", ".", ".", "8", ".", ".", "7", "9"]]
        self.assertTrue(SudokuSolver(board).place_in_square('1', 1, 1))
        self.assertFalse(SudokuSolver(board).place_in_square('6', 1, 1))
        self.assertTrue(SudokuSolver(board).place_in_square('8', 2, 5))
        self.assertTrue(SudokuSolver(board).place_in_square('2', 2, 5))
        self.assertTrue(SudokuSolver(board).place_in_square('2', 2, 8))
        self.assertFalse(SudokuSolver(board).place_in_square('6', 2, 8))

    def test_case_1(self):
        board = [["5", "3", ".", ".", "7", ".", ".", ".", "."],
                 ["6", ".", ".", "1", "9", "5", ".", ".", "."],
                 [".", "9", "8", ".", ".", ".", ".", "6", "."],
                 ["8", ".", ".", ".", "6", ".", ".", ".", "3"],
                 ["4", ".", ".", "8", ".", "3", ".", ".", "1"],
                 ["7", ".", ".", ".", "2", ".", ".", ".", "6"],
                 [".", "6", ".", ".", ".", ".", "2", "8", "."],
                 [".", ".", ".", "4", "1", "9", ".", ".", "5"],
                 [".", ".", ".", ".", "8", ".", ".", "7", "9"]]
        result = [["5", "3", "4", "6", "7", "8", "9", "1", "2"],
                  ["6", "7", "2", "1", "9", "5", "3", "4", "8"],
                  ["1", "9", "8", "3", "4", "2", "5", "6", "7"],
                  ["8", "5", "9", "7", "6", "1", "4", "2", "3"],
                  ["4", "2", "6", "8", "5", "3", "7", "9", "1"],
                  ["7", "1", "3", "9", "2", "4", "8", "5", "6"],
                  ["9", "6", "1", "5", "3", "7", "2", "8", "4"],
                  ["2", "8", "7", "4", "1", "9", "6", "3", "5"],
                  ["3", "4", "5", "2", "8", "6", "1", "7", "9"]]
        SudokuSolver().solveSudoku(board)
        self.assertEqual(board, result)

